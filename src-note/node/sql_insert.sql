-- Calculate the next available number for the barcode
SET @current_id = (
    SELECT COALESCE(MAX(CAST(SUBSTRING(barcode, 4) AS UNSIGNED)), 0) + 1 
    FROM product 
    WHERE barcode LIKE CONCAT(@prefix, '%')
);

-- Generate the new barcode in the format "PRI001"
SET @new_barcode = CONCAT(@prefix, LPAD(@current_id, 3, '0'));

